module NativeAotPacks

open System.IO
open System.Text.Json
open System.Text.Json.Serialization
open System.Collections.Generic
open System.Text

type NativeAotPack =
    { ilcompiler_id: string
      ilcompiler_version: string
      [<JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)>]
      runtime_id: string
      [<JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)>]
      runtime_version: string }

let private nativeaotPackLabel tfm rid =
    $"//dotnet/private/sdk/nativeaot_packs:{tfm}_{rid}"

let updateNativeAotPacks nativeAotPacksFile =
    let nativeAotPacks = File.ReadAllText nativeAotPacksFile

    let nativeAotPacks =
        JsonSerializer.Deserialize<Dictionary<string, Dictionary<string, NativeAotPack>>>(nativeAotPacks)

    let updatedNativeAotPacks = Dictionary<string, Dictionary<string, NativeAotPack>>()

    for tfmPacks in nativeAotPacks do
        let updatedRidPacks = Dictionary<string, NativeAotPack>()

        for ridPacks in tfmPacks.Value do
            let pack = ridPacks.Value
            let majorVersion = pack.ilcompiler_version.Split('.')[0]
            let featureVersion = pack.ilcompiler_version.Split('.')[1]

            let latestIlcVersion =
                NugetHelpers.getAllVersions pack.ilcompiler_id
                |> List.filter (fun v -> v.ToFullString().StartsWith($"{majorVersion}.{featureVersion}"))
                |> List.max

            let updatedPack =
                if pack.runtime_id <> null then
                    let latestRuntimeVersion =
                        NugetHelpers.getAllVersions pack.runtime_id
                        |> List.filter (fun v -> v.ToFullString().StartsWith($"{majorVersion}.{featureVersion}"))
                        |> List.max

                    { pack with
                        ilcompiler_version = latestIlcVersion.ToFullString()
                        runtime_version = latestRuntimeVersion.ToFullString() }
                else
                    { pack with
                        ilcompiler_version = latestIlcVersion.ToFullString() }

            updatedRidPacks.Add(ridPacks.Key, updatedPack)

        updatedNativeAotPacks.Add(tfmPacks.Key, updatedRidPacks)

    let updatedJson =
        JsonSerializer.Serialize(updatedNativeAotPacks, options = JsonSerializerOptions(WriteIndented = true))

    File.WriteAllText(nativeAotPacksFile, updatedJson)
    ()

let writeNativeAotPackLookupTable nativeAotPacksFile output =
    let json = File.ReadAllText nativeAotPacksFile

    let nativeAotPacks =
        JsonSerializer.Deserialize<Dictionary<string, Dictionary<string, NativeAotPack>>>(json)

    let lookupTable = Dictionary<string, Dictionary<string, string>>()

    for tfmPacks in nativeAotPacks do
        lookupTable.Add(tfmPacks.Key, Dictionary<string, string>())

        for ridPacks in tfmPacks.Value do
            let label = nativeaotPackLabel tfmPacks.Key ridPacks.Key
            lookupTable.[tfmPacks.Key].Add(ridPacks.Key, label)

    let lookupTableJson =
        JsonSerializer.Serialize(lookupTable, options = JsonSerializerOptions(WriteIndented = true))

    let sb = new StringBuilder()

    sb.AppendLine("\"GENERATED BY SDK GENERATOR\"") |> ignore
    sb.AppendLine() |> ignore
    sb.Append("nativeaot_pack_lookup_table = ") |> ignore
    sb.Append(lookupTableJson) |> ignore
    sb.Append("\n") |> ignore

    File.WriteAllText(output, sb.ToString())
    ()

let generateNativeAotPackTargets nativeAotPacksFile output =
    let json = File.ReadAllText nativeAotPacksFile

    let nativeAotPacks =
        JsonSerializer.Deserialize<Dictionary<string, Dictionary<string, NativeAotPack>>>(json)

    let sb = new StringBuilder()

    sb.AppendLine("\"GENERATED BY SDK GENERATOR\"") |> ignore
    sb.AppendLine() |> ignore

    sb.AppendLine("load(\"//dotnet/private/sdk/nativeaot_packs:nativeaot_nuget_pack.bzl\", \"nativeaot_nuget_pack\")")
    |> ignore

    sb.AppendLine() |> ignore
    sb.AppendLine("# buildifier: disable=unnamed-macro") |> ignore
    sb.AppendLine("def nativeaot_packs():") |> ignore
    sb.AppendLine("    \"\"\"NativeAOT pack targets\"\"\"") |> ignore
    sb.AppendLine() |> ignore

    for tfmPacks in nativeAotPacks do
        let tfm = tfmPacks.Key
        sb.AppendLine($"    # {tfm}") |> ignore

        for ridPacks in tfmPacks.Value do
            let rid = ridPacks.Key
            let pack = ridPacks.Value
            let label = $"{tfm}_{rid}"

            let ilcPack =
                $"\"@dotnet.nativeaot_packs//{pack.ilcompiler_id.ToLower()}.v{pack.ilcompiler_version}:files\""

            if pack.runtime_id <> null then
                let rtPack =
                    $"\"@dotnet.nativeaot_packs//{pack.runtime_id.ToLower()}.v{pack.runtime_version}:files\""

                sb.AppendLine(
                    $"    nativeaot_nuget_pack(name = \"{label}\", ilcompiler_pack = {ilcPack}, runtime_pack = {rtPack}, target_framework = \"{tfm}\", runtime_identifier = \"{rid}\")"
                )
                |> ignore
            else
                sb.AppendLine(
                    $"    nativeaot_nuget_pack(name = \"{label}\", ilcompiler_pack = {ilcPack}, target_framework = \"{tfm}\", runtime_identifier = \"{rid}\")"
                )
                |> ignore

        sb.AppendLine() |> ignore

    File.WriteAllText(output, sb.ToString())
    ()

let generateNativeAotPacksNugetRepo nativeAotPacksFile outputFolder =
    let json = File.ReadAllText nativeAotPacksFile

    let nativeAotPacks =
        JsonSerializer.Deserialize<Dictionary<string, Dictionary<string, NativeAotPack>>>(json)

    // Collect all unique NuGet packages (ILCompiler + runtime packs)
    let allPackages = List<struct (string * string)>()

    for tfmPacks in nativeAotPacks do
        for ridPacks in tfmPacks.Value do
            let pack = ridPacks.Value
            allPackages.Add(struct (pack.ilcompiler_id, pack.ilcompiler_version))

            if pack.runtime_id <> null then
                allPackages.Add(struct (pack.runtime_id, pack.runtime_version))

    let repoPackages: NugetRepo.NugetRepoPackage seq =
        allPackages
        |> Seq.distinctBy (fun struct (id, version) -> $"{id}.{version}")
        |> Seq.map (fun struct (id, version) ->
            let packageInfo =
                NugetHelpers.getPackageInfo id version NugetHelpers.nugetV3Feed

            { name = $"{id.ToLower()}.v{version}"
              id = id
              version = version
              sha512 = packageInfo.sha512sri
              sources = [ NugetHelpers.nugetV3Feed ]
              netrc = None
              dependencies = Dictionary<string, string seq>()
              targeting_pack_overrides = packageInfo.overrides
              framework_list = packageInfo.frameworkList
              tools = Dictionary() })

    NugetRepo.generateBazelFiles "nativeaot_packs" repoPackages outputFolder "dotnet."

    // Append ILLink analyzer nuget_archive calls to the generated .bzl file.
    // ILLink analyzers are per-TFM, with one version per major.minor release
    // matching the ILCompiler version for that TFM.
    let illinkVersions =
        nativeAotPacks
        |> Seq.map (fun tfmPacks ->
            let firstPack = tfmPacks.Value |> Seq.head
            firstPack.Value.ilcompiler_version)
        |> Seq.distinct
        |> Seq.toList

    let bzlFile = $"{outputFolder}/dotnet.nativeaot_packs.bzl"
    let existingContent = File.ReadAllText(bzlFile)

    // Add the extra loads at the top
    let updatedContent =
        existingContent.Replace(
            "load(\"@rules_dotnet//dotnet:defs.bzl\", \"nuget_repo\")",
            "load(\"@bazel_tools//tools/build_defs/repo:utils.bzl\", \"maybe\")\nload(\"@rules_dotnet//dotnet:defs.bzl\", \"nuget_repo\")\nload(\"@rules_dotnet//dotnet/private/rules/nuget:nuget_archive.bzl\", \"nuget_archive\")"
        )

    // Append ILLink analyzer entries before the end of the function
    let sb = new StringBuilder(updatedContent.TrimEnd())

    sb.AppendLine() |> ignore
    sb.AppendLine() |> ignore

    for version in illinkVersions do
        let packageInfo =
            NugetHelpers.getPackageInfo "microsoft.net.illink.tasks" version NugetHelpers.nugetV3Feed

        sb.AppendLine($"    maybe(") |> ignore
        sb.AppendLine($"        nuget_archive,") |> ignore
        sb.AppendLine($"        name = \"nuget.microsoft.net.illink.tasks.v{version}\",") |> ignore
        sb.AppendLine($"        id = \"microsoft.net.illink.tasks\",") |> ignore
        sb.AppendLine($"        version = \"{version}\",") |> ignore
        sb.AppendLine($"        sha512 = \"{packageInfo.sha512sri}\",") |> ignore
        sb.AppendLine($"        sources = [\"https://api.nuget.org/v3/index.json\"],") |> ignore
        sb.AppendLine($"    )") |> ignore

    File.WriteAllText(bzlFile, sb.ToString())
    ()
